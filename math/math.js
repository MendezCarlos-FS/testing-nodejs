function add(x, y) {
    return x + y;
};

function subtract(x, y){
    return x - y;
};

function multiply(x, y) {
    return x * y;
};

function divide(x, y) {
    return x / y;
};

function squareRoot(x) {
    return Math.sqrt(x);
};

function max(x, y) {
    return Math.max(x, y);
};

module.exports = { add, subtract, multiply, divide, squareRoot, max };